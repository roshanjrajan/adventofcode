class CPU():
    def __init__(self):
        self.X = 1
        self.clock = 0
        self.crt_pos = 0
        self.crt_drawing = ""
        self.signal_strength = 0

    def update(self, X = 0):
        self.clock += 1
        if (self.clock - 20) % 40 == 0:
            # print(self.clock, self.X, self.X*self.clock)
            self.signal_strength += self.X * self.clock
        self.X += X

def compute(s: str) -> int:

    lines = s.splitlines()
    cpu = CPU()
    for line in lines:
        instr, *value = line.split()
        match instr:
            case "noop":
                cpu.update()
            case "addx":
                cpu.update()
                cpu.update(int(value[0]))

    return cpu.signal_strength


